void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)       
{
    if(huart->Instance==LPUART1)
    {
        if(__HAL_UART_GET_FLAG(&hlpuart1,UART_FLAG_ORE))
        {
            __HAL_UART_CLEAR_FLAG(&hlpuart1,UART_FLAG_ORE);
            __HAL_UART_CLEAR_OREFLAG(&hlpuart1);
        }
        if(__HAL_UART_GET_IT(&hlpuart1, UART_IT_RXNE ) != RESET)
        {
            __HAL_UART_CLEAR_FLAG(&hlpuart1,UART_FLAG_RXNE);
            if(recvlp1_num < LPUART1_BUFFER_SIZE)
            {   

                recvlp1_buf[recvlp1_num++] = LPUART1 -> RDR;    
            }
            else
            {
                recvlp1_num = 0;
            }
            
           
        }
        if(__HAL_UART_GET_FLAG(&hlpuart1,UART_FLAG_IDLE) != RESET)
        {
            HAL_UART_Transmit(&huart2, (uint8_t *)recvlp1_buf, recvlp1_num, 0xFFFF);
            recframe_done = 1;         //接收完一帧数据
//                LED_OFF;
            recvlp1_num = 0;  
            __HAL_UART_CLEAR_IDLEFLAG(&hlpuart1);
        }
        
    }

}
